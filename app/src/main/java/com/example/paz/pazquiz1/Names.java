package com.example.paz.pazquiz1;


public class Names {
    private int logo;
    private String name;

    public Names(int logo, String name) {
        this.logo = logo;
        this.name = name;
    }


    public int getLogo() {
        return logo;
    }

    public void setLogo(int logo) {
        this.logo = logo;
    }

    public String getname() {
        return name;
    }

    public void setname(String name) {
        this.name = name;
    }
}
